from PIL import Image
import os

# Define the sizes you need
sizes = [
    16, 50, 66, 92, 128, 196, 1024, 20, 55, 72, 100, 5, 144, 216,
    29, 57, 76, 102, 152, 234, 32, 58, 80, 108, 167, 256,
    40, 60, 87, 114, 172, 258, 48, 64, 88, 120, 180, 512
]

# Load the original image
original_image_path = "1024.png"  # Ensure this image exists in the script directory
output_folder = "resized_images"
os.makedirs(output_folder, exist_ok=True)

# Open the original image
with Image.open(original_image_path) as img:
    for size in sizes:
        resized_img = img.resize((size, size), Image.ANTIALIAS)
        output_path = os.path.join(output_folder, f"{size}.png")
        resized_img.save(output_path)

print(f"Resized images saved in {output_folder}")


